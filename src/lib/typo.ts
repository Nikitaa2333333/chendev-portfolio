// ============================================================
// Типографика: висячие предлоги/союзы.
// ЖЁСТКОЕ ПРАВИЛО дизайн-системы (CLAUDE.md): короткое слово (предлог,
// союз, 1–2 буквы) не должно оставаться в конце строки — приклеиваем его
// к следующему слову неразрывным пробелом ( ).
// Применять ко ВСЕМУ видимому русскому тексту: {typo('…')} в .astro.
// Перевод строки (\n, разбивка на абзацы) не трогаем — клеим только пробелы.
// ============================================================

const NBSP = String.fromCharCode(160); // 160 = U+00A0 неразрывный пробел (чистый ASCII, не нормализуется сборкой)

// Оба паттерна — regex-ЛИТЕРАЛЫ (надёжное экранирование \s; через new RegExp
// + template literal \s ломался). Разделитель слева: начало, пробел, скобка,
// кавычка, тире. Справа — один или несколько обычных пробелов (не \n!).
const RE_SHORT = /(?<=^|[ \t(«„"—–-])([A-Za-zА-Яа-яЁё]{1,2}) +/g;
const RE_LONG = /(?<=^|[ \t(«„"—–-])(для|что|как|или|при|под|над|без|про|это|так|где|чем|тем|уже|его|её|они|оно|она) +/gi;

export function typo(input: string): string {
  if (!input) return input;
  return input.replace(RE_SHORT, `$1${NBSP}`).replace(RE_LONG, `$1${NBSP}`);
}
